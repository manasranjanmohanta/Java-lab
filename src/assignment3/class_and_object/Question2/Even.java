package assignment3.class_and_object.Question2;

public class Even {
    int n ;
    Even(int num){
        n = num;
    }
    public void isEven(){
        if(n % 2 == 0){
            System.out.println(n + " is even");
        } else {
            System.out.println(n + " is even");
        }
    }

}
